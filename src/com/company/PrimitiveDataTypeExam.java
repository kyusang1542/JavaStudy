package com.company;

public class PrimitiveDataTypeExam {
    // 기본형 타입 종류
    public static void main(String[] args) {
        boolean isFun = true; // 또는 false
        System.out.println(isFun);

        char c = 'f';   // 반드시 작은 따옴표를 사용해야함

        int x = 59;

        // long 데이터 타입은 뒤에다 반드시 L을 넣어야함 (소문자 l가능)
        long bing = 3453545354L;

        // float 데이터 타입은 뒤에다 반드시 f을 넣어야함 (대문자 F가능)
        float f = 32.4f;

        double d = 324566.5;
    }
}
